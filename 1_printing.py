
#printing

# print string
print ('Hello guys!!')

# print number
print (1)
print (1, 2, 3, 4, 5)

# print with custom seperator
print (1, 2, 3, 4, 5, sep='#')
print (1, 2, 3, 4, 5, sep=" (^.^) ")

# print with custom end character
print('a', end='')
print('b', end='')
print('c', end='')

# single/double quotes are okay
print ('Nama saya "Budi"')
print ("Nama saya 'Budi'")

# escape sequence characters
'''
\n newline
\t tab
\r carriage return
\b backspace
'''
print ('12345\n6789')
print ('12345\t6789')
print ('12345\r6789')
print ('12345\b6789')
